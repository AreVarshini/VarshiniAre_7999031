class TypeCasting {
public static void main(String args[]) {
double x=99.99;
int y=(int)x;
System.out.println("Double value: " + x);
System.out.println("Converted to Integer: " + y);
int a=10;
double b=a;
System.out.println("Integer value: " + a);
System.out.println("Converted to Double: " + b);
}
}

